pub fn tile_at(x: i32, y: i32) -> i32 {
    if x < 0 || y < 0 || x >= MAP_SIZE as i32 || y >= MAP_SIZE as i32 {
        return 1;
    }
    WORLD_MAP[x as usize][y as usize]
}